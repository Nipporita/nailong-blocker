# 复用当前 pattern 集
from nailong_patterns import PATTERNS, match_any

def prep(text):
    text = unicodedata.normalize("NFKC", text)
    text = re.sub(r"[​-‏⁠-⁯﻿]", "", text)
    # 也去软连字符
    text = text.replace("­", "")
    return text

def test(text, expect=True):
    got = match_any(prep(text))
    status = "BLOCKED" if got else "PASSED THROUGH !"
    return got == expect, status, got

